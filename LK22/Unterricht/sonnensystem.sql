DROP SCHEMA IF EXISTS sonnensystem;
CREATE SCHEMA sonnensystem DEFAULT CHARACTER SET utf8;
USE sonnensystem;

CREATE TABLE planet (
	position INT NOT NULL,
	name VARCHAR(20) NOT NULL,
	bahnradius_ae DOUBLE NOT NULL,          # ae = Astronomische Einheit
	aequator_radius_er DOUBLE NOT NULL,     # er = earth radius
	masse_em DOUBLE NOT NULL,               # em = earth mass
	fallbeschleunigung DOUBLE NOT NULL,
	umlaufzeit_ey DOUBLE NOT NULL,          # ey = earth year
	tageslaenge_ed DOUBLE NOT NULL,         # ed = earth day
	typ ENUM('Gesteinsplanet', 'Gasplanet') NOT NULL,
	anzahl_monde INT NOT NULL,
	ringe ENUM('Ja', 'Nein'),
	entdeckungsjahr INT,
	PRIMARY KEY (position)
);

CREATE TABLE trabant (
	trabant_id INT AUTO_INCREMENT,
	planet INT NOT NULL,
	position INT NOT NULL,
	name VARCHAR(20) NOT NULL,
	bahnradius_km DOUBLE NOT NULL,
	durchmesser_km DOUBLE,
	masse_kg DOUBLE,
	fallbeschleunigung DOUBLE,	
	umlaufzeit_ed DOUBLE         ,          # ed = earth day
	entdeckungsjahr INT,
	PRIMARY KEY (trabant_id),
	FOREIGN KEY (planet) REFERENCES planet (position)
);

CREATE TABLE zwergplanet (
	zwergplanet_id INT NOT NULL,
	name VARCHAR(20) NOT NULL,
	bahnradius_ae DOUBLE NOT NULL,          # ae = Astronomische Einheit
	aequator_radius_km DOUBLE NOT NULL,
	masse_kg DOUBLE NOT NULL,
	fallbeschleunigung DOUBLE,	
	umlaufzeit_ey DOUBLE NOT NULL,          # ey = earth years
	tageslaenge_ed DOUBLE,                  # ed = earth days
	orbittyp ENUM('Asteroidengürtel', 'Transneptunisch'),
	anzahl_monde INT NOT NULL,
	entdeckungsjahr INT NOT NULL,
	PRIMARY KEY (zwergplanet_id)
);
    
CREATE TABLE asteroid (
	nummer INT NOT NULL,
	name VARCHAR(20) NOT NULL,
	bahnradius_ae DOUBLE NOT NULL,          # ae = Astronomische Einheit
	durchmesser_km DOUBLE NOT NULL,
	masse_kg DOUBLE NOT NULL,
	umlaufzeit_ey DOUBLE NOT NULL,          # ey = earth year
	orbittyp ENUM('Erdnah', 'Hauptgürtel', 'Transneptunisch'),
	entdeckungsjahr INT NOT NULL,
	PRIMARY KEY (nummer)
);

